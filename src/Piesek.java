public class Piesek {
   private String name;
   private  String rasa;
    private String color;

    public void setName(String name) {
        this.name = name;
    }
    public void setRasa(String rasa) {
        this.rasa = rasa;
    }

    public String getName() {
        return name;
    }

    public String getRasa() {
        return rasa;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
